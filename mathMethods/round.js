// round()

// it converts the float value depends on value it rounds the value.
// ex:-   0.1 is 0
 //       0.4 is 0
 //       0.5 is 1

console.log(Math.PI)
console.log(Math.round(Math.PI))   //3
console.log(Math.round(0.2))       // 0
console.log(Math.round(0.5))       // 1
console.log(Math.round(0.9))       // 1