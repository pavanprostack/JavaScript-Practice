// verifying random numbers using switch statement

switch (Math.floor(Math.random() * 4 + 1)) {
    case 1:
        console.log(1);
        break;
    case 2:
        console.log(2);
        break;
    case 2:
        console.log(3);
        break;
    case 2:
        console.log(4);
        break;
    default:
        console.log("No match")
}